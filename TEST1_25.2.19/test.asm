SSEG SEGMENT PARA STACK 'STACK'
	DB 64 DUP(0)
SSEG ENDS

DSEG SEGMENT PARA 'DATA'
	X DB 'A',0
DSEG ENDS

CSEG SEGMENT PARA 'CODE'
	ASSUME CS:CSEG, DS:DSEG, SS:SSEG

; ENTRY POINT
MAIN:
	MOV AX, DSEG
	MOV DS, AX
	
	; PRINTING CHARS FROM A TO D
	MOV CX, 5
	LOOP PRINT_ASCII		

	JMP EXIT

PRINT_ASCII:
	JMP PRINT_SYMBOL_FROM_DL
	INC X
	RET
		
; OUTPUT SYMBOLS
PRINT_SYMBOL_FROM_DL:
	MOV AH, 02H
	INT 21H
	RET

; EXIT APPLICATION
EXIT:
	MOV AH, 4CH
	INT 21H
	
CSEG ENDS
END MAIN